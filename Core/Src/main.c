void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
  if(GPIO_Pin == GIRIS_Pin)
  {
    if((arac_sayisi < 10) && (acil == 0))
    {
      arac_sayisi++;
      bariyer_ac();
      bariyer_kapanma_zamani = saniye + 2;
      uart_yaz("GIRIS OLDU\r\n");
    }
    else if(arac_sayisi >= 10)
    {
      uart_yaz("OTOPARK DOLU\r\n");
    }
  }

  if(GPIO_Pin == CIKIS_Pin)
  {
    if((arac_sayisi > 0) && (acil == 0))
    {
      arac_sayisi--;
      bariyer_ac();
      bariyer_kapanma_zamani = saniye + 2;
      uart_yaz("CIKIS OLDU\r\n");
    }
  }

  if(GPIO_Pin == ACIL_BUTTON_Pin)
  {
    acil = 1;
    bariyer_kapat();
    uart_yaz("ACIL DURUM AKTIF\r\n");
  }
}

